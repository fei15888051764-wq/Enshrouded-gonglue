export function getBossDetailKey(name: string): string {
  return name.toLowerCase()
    .replace(/'/g, '-')   // Hydrak'Dal → hydrak-dal
    .replace(/\s+/g, '-')
    .replace(/[^a-z0-9-]/g, '');
}
